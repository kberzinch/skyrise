task autonomous {
	motor[CollectionA] = 127;
	motor[CollectionB] = 127;
	motor[LiftLeftA] = -127;
	motor[LiftLeftB] = -127;
	motor[LiftRightA] = -127;
	motor[LiftRightB] = -127;
	wait1Msec(1000);
	motor[LiftLeftA] = 0;
	motor[LiftLeftB] = 0;
	motor[LiftRightA] = 0;
	motor[LiftRightB] = 0;
	motor[CollectionA] = 0;
	motor[CollectionB] = 0;
	motor[DriveFrontLeft] = -127;
	motor[DriveRearLeft] = -127;
	motor[DriveFrontRight] = -127;
	motor[DriveRearRight] = -127;
	wait1Msec(1000);
	allMotorsOff();
}

float multiplier = 1;

task usercontrol {
	while(true) {
		if(vexRT[Btn8D] == 1) {
			multiplier = 0.2;
		} else {
			multiplier = 1;
		}
		motor[DriveFrontLeft] = (vexRT[Ch3] + vexRT[Ch4]) * multiplier;
		motor[DriveFrontRight] = (vexRT[Ch3] - vexRT[Ch4]) * multiplier;
		motor[DriveRearLeft] = (vexRT[Ch3] + vexRT[Ch4]) * multiplier;
		motor[DriveRearRight] = (vexRT[Ch3] - vexRT[Ch4]) * multiplier;

		// LIFT
		if(vexRT[Btn6D] == 1) {
			motor[LiftLeftA] = 127;
			motor[LiftLeftB] = 127;
			motor[LiftRightA] = 127;
			motor[LiftRightB] = 127;
			} else if(vexRT[Btn6U] == 1 && SensorValue[LiftLimit] == 0) {
			motor[LiftLeftA] = -127;
			motor[LiftLeftB] = -127;
			motor[LiftRightA] = -127;
			motor[LiftRightB] = -127;
			} else {
			motor[LiftLeftA] = 0;
			motor[LiftLeftB] = 0;
			motor[LiftRightA] = 0;
			motor[LiftRightB] = 0;
		}

		// COLLECTION
		if(vexRT[Btn5D] == 1) {
			motor[CollectionA] = 127;
			motor[CollectionB] = 127;
			} else if(vexRT[Btn5U] == 1) {
			motor[CollectionA] = -127;
			motor[CollectionB] = -127;
			} else {
			motor[CollectionA] = 0;
			motor[CollectionB] = 0;
		}
	}
}
